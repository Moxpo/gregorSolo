public class Person {
    private String name;
    private int age;
    private String favColour;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getFavColour() {
        return favColour;
    }

    public void setFavColour(String favColour) {
        this.favColour = favColour;
    }
}
